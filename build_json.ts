interface Name {
    id: string; key: string; name: string; value: string; brand: string; type: string;
}

const entries: Name[] = [];
for (let i = 0; i < names.length; i++) {
    const curName = names[i];
    const curColor = colors[i];
    const parsedName = curName.match(/^(?<key>.+?)-(?<id>.+?)\s+(?<name>.+)$/);
    const { key, id, name } = parsedName!.groups as any as Name;
    entries.push({
        id,
        key,
        name,
        brand: 'Tamiya',
        type: 'Acrylic',
        value: curColor
    });
}

await Deno.writeTextFile('./tamiya_acrylic.json', JSON.stringify(entries));